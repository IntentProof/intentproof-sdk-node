#!/usr/bin/env node
/**
 * Materialize JSON Schemas from intentproof-spec as TypeScript modules for Ajv (deterministic output).
 * intentproof_config's external wrap_options ref is inlined (same strategy as Python codegen).
 */
import stringify from "json-stable-stringify";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
let specRoot = process.env.INTENTPROOF_SPEC_ROOT;
if (!specRoot) {
  const sibling = join(repoRoot, "../intentproof-spec");
  if (existsSync(join(sibling, "spec.json"))) {
    specRoot = sibling;
  }
}
if (!specRoot || !existsSync(join(specRoot, "spec.json"))) {
  console.error(
    "embed-spec-schemas: set INTENTPROOF_SPEC_ROOT or clone intentproof-spec next to intentproof-sdk-node",
  );
  process.exit(1);
}

const schemaDir = join(specRoot, "schema");
const outDir = join(repoRoot, "packages/sdk/src/generated/embed");

function emitEmbed(baseName, obj) {
  const body = stringify(obj);
  const ts = `/* eslint-disable */\n/**\n * Normative JSON Schema from intentproof-spec (bundled for Ajv).\n * Generated by scripts/embed-spec-schemas.mjs — do not edit.\n */\nexport default ${body} as const;\n`;
  writeFileSync(join(outDir, `${baseName}.ts`), ts, "utf8");
}

mkdirSync(outDir, { recursive: true });

const executionEvent = JSON.parse(
  readFileSync(join(schemaDir, "execution_event.v1.schema.json"), "utf8"),
);
emitEmbed("execution-event.v1", executionEvent);

const wrapOptions = JSON.parse(
  readFileSync(join(schemaDir, "wrap_options.v1.schema.json"), "utf8"),
);
emitEmbed("wrap-options.v1", wrapOptions);

const intentproofConfig = JSON.parse(
  readFileSync(join(schemaDir, "intentproof_config.v1.schema.json"), "utf8"),
);
intentproofConfig.$defs = intentproofConfig.$defs || {};
intentproofConfig.$defs.WrapOptionsV1 = wrapOptions;
if (intentproofConfig.properties?.defaultWrapOptions) {
  intentproofConfig.properties.defaultWrapOptions = { $ref: "#/$defs/WrapOptionsV1" };
}
emitEmbed("intentproof-config.v1", intentproofConfig);

console.error(`Wrote schema embeds under ${outDir}`);
